@app.route('/register', methods=["GET", "POST"])
def register():
    form = UserForm()
    if form.validate_on_submit():
        hashed_password = generate_password_hash(form.password.data, method='scrypt', salt_length=16)
        password_hash = hashed_password.split('$')[1] + '$' + hashed_password.split('$')[2]
        username = form.username.data
        email = form.email.data
        if not User.query.filter_by(username=username).filter_by(email=email).first():
            new_user = User(username=username, email=email, password=password_hash)
            db.session.add(new_user)
            db.session.commit()
            return redirect(url_for('get_all_posts'))
    return render_template("register.html", form=form)


